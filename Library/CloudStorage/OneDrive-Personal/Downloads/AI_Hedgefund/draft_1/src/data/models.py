@dataclass
class Position:
    """Current position in a market"""
    market_id: str
    outcome: Outcome
    contracts: int
    avg_entry_price: float
    entry_timestamp: datetime
    total_invested: float = 0.0
    fills: List["Fill"] = field(default_factory=list)  # Track fills that created this position
    conflict_ids: List[str] = field(default_factory=list)  # Link to conflicts

    @property
    def current_value_at_price(self, current_price: float) -> float:
        """Position value at given price"""
        if self.contracts == 0:
            return 0.0
        return self.contracts * current_price

    @property
    def unrealized_pnl_at_price(self, current_price: float) -> float:
        """Unrealized P&L at given price"""
        return self.current_value_at_price(current_price) - self.total_invested

    @property
    def unrealized_return_at_price(self, current_price: float) -> float:
        """Unrealized return % at given price"""
        if self.total_invested == 0:
            return 0.0
        return self.unrealized_pnl_at_price(current_price) / self.total_invested

    def calculate_settlement_pnl(self, winning_outcome: Outcome) -> float:
        """
        Calculate P&L based on market outcome.
        Winners get $1/contract, losers get $0.

        Args:
            winning_outcome: The outcome that won (YES or NO)

        Returns:
            Settlement P&L (payout - total_invested)
        """
        if self.contracts == 0:
            return 0.0

        # If we hold the winning outcome, we get $1 per contract
        if self.outcome == winning_outcome:
            payout = abs(self.contracts) * 1.0  # $1 per contract
        else:
            payout = 0.0  # Losing outcome gets $0

        # P&L = Payout - Total Invested
        return payout - self.total_invested


@dataclass
class Portfolio:
    """Portfolio state and metrics"""
    timestamp: datetime
    cash: float
    positions: dict = field(default_factory=dict)  # market_id -> Position
    closed_trades: list = field(default_factory=list)
    total_invested: float = 0.0

    @property
    def total_value(self) -> float:
        """Total portfolio value (cash + positions)"""
        return self.cash + sum(p.total_invested for p in self.positions.values())

    @property
    def position_count(self) -> int:
        """Number of open positions"""
        return len([p for p in self.positions.values() if p.contracts > 0])

    def get_position(self, market_id: str, outcome: Outcome) -> Optional[Position]:
        """Get position for specific market/outcome"""
        key = f"{market_id}:{outcome.value}"
        return self.positions.get(key)


@dataclass
class BacktestResult:
    """Backtest execution results"""
    strategy_name: str
    trades: list  # List of executed trades
    equity_curve: list  # List of (timestamp, equity) tuples
    metrics: dict  # Performance metrics

    @property
    def total_trades(self) -> int:
        return len(self.trades)

    @property
    def winning_trades(self) -> int:
        count = 0
        for t in self.trades:
            # Handle both Trade objects and dicts
            pnl = t.pnl if hasattr(t, 'pnl') else t.get('pnl', 0)
            if pnl > 0:
                count += 1
        return count

    @property
    def win_rate(self) -> float:
        if self.total_trades == 0:
            return 0.0
        return self.winning_trades / self.total_trades

    @property
    def total_return(self) -> float:
        return self.metrics.get('total_return', 0.0)

    @property
    def sharpe_ratio(self) -> float:
        return self.metrics.get('sharpe_ratio', 0.0)

    @property
    def max_drawdown(self) -> float:
        return self.metrics.get('max_drawdown', 0.0)
