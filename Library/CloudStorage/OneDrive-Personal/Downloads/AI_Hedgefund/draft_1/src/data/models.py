"""Core data models for prediction market trading"""

from dataclasses import dataclass, field
from datetime import datetime
from typing import Optional, List
from enum import Enum


class Direction(str, Enum):
    """Trade direction"""
    BUY = "BUY"
    SELL = "SELL"


class Outcome(str, Enum):
    """Contract outcome"""
    YES = "YES"
    NO = "NO"


@dataclass
class MarketTick:
    """Single market tick/snapshot"""
    timestamp: datetime
    market_id: str
    exchange: str  # 'kalshi', 'polymarket'
    yes_bid: float
    yes_ask: float
    no_bid: float
    no_ask: float
    volume_24h: Optional[int] = None
    last_price: Optional[float] = None
    outcome: Optional["Outcome"] = None  # YES/NO winner for resolved markets
    close_time: Optional[datetime] = None  # When market closes/resolves
    is_resolved: bool = False  # Whether market has resolved

    @property
    def yes_mid(self) -> float:
        """Midpoint for YES contract"""
        return (self.yes_bid + self.yes_ask) / 2

    @property
    def no_mid(self) -> float:
        """Midpoint for NO contract"""
        return (self.no_bid + self.no_ask) / 2

    @property
    def yes_spread(self) -> float:
        """Spread for YES contract"""
        return self.yes_ask - self.yes_bid

    @property
    def no_spread(self) -> float:
        """Spread for NO contract"""
        return self.no_ask - self.no_bid

    @property
    def total_cost(self) -> float:
        """Cost of matched pair (YES + NO)"""
        return self.yes_mid + self.no_mid

    @property
    def arbitrage_spread(self) -> float:
        """Potential arbitrage spread if total_cost < 1.0"""
        return 1.0 - self.total_cost


@dataclass
class Trade:
    """Historical trade execution"""
    trade_id: str
    timestamp: datetime
    market_id: str
    direction: Direction
    outcome: Outcome
    contracts: int
    price: float
    cost: float
    trader_id: str

    @property
    def pnl_per_contract(self) -> float:
        """Maximum P&L per contract (if outcome wins)"""
        if self.outcome == Outcome.YES and self.direction == Direction.BUY:
            return 1.0 - self.price
        elif self.outcome == Outcome.YES and self.direction == Direction.SELL:
            return self.price
        elif self.outcome == Outcome.NO and self.direction == Direction.BUY:
            return 1.0 - self.price
        else:  # NO and SELL
            return self.price

    @property
    def max_pnl(self) -> float:
        """Maximum total P&L if outcome wins"""
        return self.pnl_per_contract * self.contracts


